-- Drop Policies if exist before creating (To avoid policy already exists error)
DROP POLICY IF EXISTS "Public read software_plans" ON public.software_plans;
DROP POLICY IF EXISTS "Users view own admin_account" ON public.admin_accounts;
DROP POLICY IF EXISTS "Super Admins manage all" ON public.admin_accounts;
DROP POLICY IF EXISTS "Super Admins manage all admin_accounts" ON public.admin_accounts;

CREATE POLICY "Public read software_plans" ON public.software_plans FOR SELECT USING (true);
CREATE POLICY "Users view own admin_account" ON public.admin_accounts 
FOR SELECT TO authenticated USING (auth.uid() = user_id);
CREATE POLICY "Super Admins manage all" ON public.admin_accounts 
FOR ALL TO authenticated USING (public.is_super_admin()) WITH CHECK (public.is_super_admin());

-- 5. TRIGGER ON SIGNUP
CREATE OR REPLACE FUNCTION public.handle_new_admin_signup()
RETURNS TRIGGER
LANGUAGE plpgsql
SECURITY DEFINER
AS $$
BEGIN
    IF NEW.raw_user_meta_data ->> 'role' = 'admin_owner' THEN
        INSERT INTO public.admin_accounts (
            user_id, email, full_name, gym_name, plan_type, plan_price
        ) VALUES (
            NEW.id,
            NEW.email,
            COALESCE(NEW.raw_user_meta_data ->> 'full_name', 'Gym Owner'),
            COALESCE(NEW.raw_user_meta_data ->> 'gym_name', 'New Gym'),
            COALESCE(NEW.raw_user_meta_data ->> 'plan_type', 'monthly'),
            COALESCE((NEW.raw_user_meta_data ->> 'plan_price')::numeric, 5000)
        ) ON CONFLICT (user_id) DO NOTHING;
    END IF;
    RETURN NEW;
END;
$$;

DROP TRIGGER IF EXISTS on_auth_user_created ON auth.users;
CREATE TRIGGER on_auth_user_created
    AFTER INSERT ON auth.users
    FOR EACH ROW EXECUTE FUNCTION public.handle_new_admin_signup();

-- 6. ENABLE REALTIME (With check to avoid error if already added)
DO $$
BEGIN
    IF NOT EXISTS (
        SELECT 1 FROM pg_publication_tables 
        WHERE pubname = 'supabase_realtime' 
        AND schemaname = 'public' 
        AND tablename = 'software_plans'
    ) THEN
        ALTER PUBLICATION supabase_realtime ADD TABLE public.software_plans;
    END IF;

    IF NOT EXISTS (
        SELECT 1 FROM pg_publication_tables 
        WHERE pubname = 'supabase_realtime' 
        AND schemaname = 'public' 
        AND tablename = 'admin_accounts'
    ) THEN
        ALTER PUBLICATION supabase_realtime ADD TABLE public.admin_accounts;
    END IF;
END $$;
